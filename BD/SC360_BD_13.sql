#DROP PROCEDURE SP_INSERTAR_DATOS_CARGA 
CREATE PROCEDURE sc360.SP_INSERTAR_DATOS_CARGA
(
IN INTIPOARCHIVO       INT,
IN INNOMBREARCHIVO     VARCHAR(100),
IN INESTADO            VARCHAR(100),
IN INCANTIDAD          INT,
IN INCANTIDADERRADOS   INT
)
BEGIN

   DECLARE CONTADOR INT;
   
   SELECT COUNT(*) INTO CONTADOR FROM CARGA WHERE TIPOARCHIVO = INTIPOARCHIVO;

    IF ( CONTADOR = 0 AND INESTADO ='I') THEN

       INSERT INTO CARGA(TIPOARCHIVO,NOMBREARCHIVO,CANTIDADREGISTROS,REGISTROSERRADOS,FECHAINICIO,ESTADO)
       VALUES(INTIPOARCHIVO,INNOMBREARCHIVO,INCANTIDAD,INCANTIDADERRADOS,NOW(),'INICIO');


    END IF;

    IF ( CONTADOR = 1 AND INESTADO ='I') THEN

       UPDATE CARGA
       SET NOMBREARCHIVO = INNOMBREARCHIVO,
           FECHAINICIO = NOW(),
           FECHAFIN = NULL,
           CANTIDADREGISTROS = INCANTIDAD,
           REGISTROSERRADOS = INCANTIDADERRADOS,
           ESTADO ='INICIO'
       WHERE TIPOARCHIVO = INTIPOARCHIVO;

    END IF;

    IF (CONTADOR=1 AND INESTADO='F') THEN

       UPDATE CARGA
       SET NOMBREARCHIVO = INNOMBREARCHIVO,
           FECHAFIN = NOW(),
           CANTIDADREGISTROS = INCANTIDAD,
           REGISTROSERRADOS = INCANTIDADERRADOS,
           ESTADO ='FINALIZADO'
       WHERE TIPOARCHIVO = INTIPOARCHIVO;

    END IF;

END; 



